// ==, ===, !=, !==, >, <, >=, <=

console.log(1 == 1); // É igual?
console.log(1 == "1"); // 1 é igual a 1, porém o type é diferente
console.log(1 === "1"); // 1 é diferente de 1, pois o type é diferente
console.log( 1 != 2); // verificando se 1 é diferente de 2
console.log( 1 != "1"); // retornar false, pois são iguais se não comparar o type
console.log( 1 !== "1"); // retornar true, pois tem type diferente
console.log(1 > 2); // É maior?
console.log(1 < 2); // É menor?
console.log( 1 >= 1); // É maior ou igual?
console.log( 123 <= 12); // É menor ou igual?